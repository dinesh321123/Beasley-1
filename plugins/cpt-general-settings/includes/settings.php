	/**
	 * Verifies whether a given attachment has an image MIME type
	 * 
	 * @param int $attachment_id The ID of the attachment to be checked
	 * @return bool True if MIME type is "image"
	 */
	public static function verify_media_is_image( $attachment_id ) {
		// Get the attachment's file type
		$mime_type = get_post_mime_type( $attachment_id );
	
		// Verify it is an image
		if ( strpos( $mime_type, 'image' ) !== false ) {
			return true;
		}
	
		return false;
	}

	/**
	 * Modified the HTML output of media items when they are being added to a post, adding an attribute to the <img> tag for easier access of an image's attribution information as stored in the post's metadata.
	 * 
	 * @param string $html The HTML content, as generated by the media library
	 * @param int $id The ID of the media item
	 * @param object $attachment WP_Attachment object for the given image
	 * @return string HTML output, potentially modified to contain a data-attribution attribute where possible
	 */
	public static function modify_media_insert_string( $html, $id, $attachment ) {
		if( self::verify_media_is_image( $id ) ) {
			$image_attribution = get_post_meta( $id, 'gmr_image_attribution', true ); // Retrieve the Image Attribution
			$html_updated = str_replace( '<img ', '<img data-media-image-attribution="'.$image_attribution.'" ', $html );
			$html_updated = str_replace( 'class="', 'class="media-image-with-attr ', $html_updated );
			return $html_updated;
		}
		return $html;
	}

